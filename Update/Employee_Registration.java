    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        try {
            String em = (String) email.getText();
        
        Pattern p = Pattern.compile("^[A-Za-z0-9]+\\@[A-Za-z0-9]+\\.[A-Za-z]{2,}$");
        Matcher m = p.matcher(em);
        if(m.matches()){
            JOptionPane.showMessageDialog(null, "Email is valid", "Correct", 1);
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/project", "root", "Rohan@2008oxford");
            PreparedStatement ps = c.prepareStatement("Insert into employee Values(?,?,?,?,?,?,?)");
            ps.setInt(1, Integer.parseInt(eid.getText()));
            ps.setString(2, name.getText());
            ps.setString(3, uname.getText());
            ps.setString(4, em);
            ps.setString(5, passcode.getText());
            ps.setDate(6, new java.sql.Date(jdate.getDate().getTime()));
            ps.setInt(7, Integer.parseInt(salary.getText()));

            ps.executeUpdate();
            JOptionPane.showConfirmDialog(null, "You Successfuly Register as a Employess", "Confirm", -1);
            shows();
        }
        else{
            JOptionPane.showConfirmDialog(null, "Email is Not Valid", "Warning", JOptionPane.WARNING_MESSAGE);
        }
           
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

